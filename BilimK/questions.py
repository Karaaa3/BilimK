def _merge_by_grade(bank_a: dict, bank_b: dict) -> dict:
    """Объединяет два банка вопросов (грейд -> список) в один, грейд за грейдом."""
    merged = {}
    for grade in GRADES:
        merged[grade] = list(bank_a.get(grade, [])) + list(bank_b.get(grade, []))
    return merged


