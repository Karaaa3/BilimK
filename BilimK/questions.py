def get_subjects(lang: str = "ru"):
    """
    Возвращает список названий предметов для нужного языка интерфейса.
    lang = "ru" -> структура русской школы (Русский язык и Русская
           литература отдельно, Қазақ тілі мен әдебиеті вместе).
    lang = "kz" -> структура казахской школы (Қазақ тілі и Қазақ
           әдебиеті отдельно, Орыс тілі мен әдебиеті вместе).
    """
    names = [(name_ru if lang == "ru" else name_kz) for (_, name_ru, name_kz) in CONTENT_SUBJECTS]

    if lang == "ru":
        names += ["Русский язык", "Русская литература", "Қазақ тілі мен әдебиеті", "Английский язык"]
    else:
        names += ["Қазақ тілі", "Қазақ әдебиеті", "Орыс тілі мен әдебиеті", "Ағылшын тілі"]

    return names


def get_questions(subject_name: str, grade: int, lang: str = "ru"):
    """Возвращает список вопросов по выбранному предмету, классу и языку интерфейса."""

    for key, name_ru, name_kz in CONTENT_SUBJECTS:
        if subject_name in (name_ru, name_kz):
            return copy.deepcopy(CONTENT_QUESTIONS.get(key, {}).get(lang, {}).get(grade, []))

    fixed_banks = {
        "Английский язык": ENGLISH_QUESTIONS,
        "Ағылшын тілі": ENGLISH_QUESTIONS,
        "Қазақ тілі": KAZ_LANG_QUESTIONS,
        "Қазақ әдебиеті": KAZ_LIT_QUESTIONS,
        "Орыс тілі мен әдебиеті": RUS_COMBINED_QUESTIONS,
        "Русский язык": RUS_LANG_QUESTIONS,
        "Русская литература": RUS_LIT_QUESTIONS,
        "Қазақ тілі мен әдебиеті": KAZ_COMBINED_QUESTIONS,
    }

    bank = fixed_banks.get(subject_name)
    if bank is not None:
        return copy.deepcopy(bank.get(grade, []))

    return []
