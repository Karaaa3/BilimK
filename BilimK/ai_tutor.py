"""
ai_tutor.py
-----------
Это "мозг" продукта — модуль ИИ-тьютора.

ЗАЧЕМ ОН НУЖЕН (для защиты перед жюри):
Обычный тест просто показывает "верно/неверно". Наш продукт решает
реальную проблему сельских школ — нехватку учителей-предметников,
которые могли бы индивидуально разобрать ошибку каждого ученика.
ИИ-тьютор выполняет именно эту функцию: не даёт готовый ответ,
а объясняет ХОД РАССУЖДЕНИЯ, как если бы рядом сидел репетитор,
а затем составляет полноценный план обучения по слабым темам.

КАК ЭТО РАБОТАЕТ ТЕХНИЧЕСКИ:
1. Мы НЕ отправляем в LLM вопрос "реши задачу" — мы уже знаем правильный
   ответ (он есть в банке вопросов). Модель не выдумывает правильный
   ответ заново (риск галлюцинаций), а только объясняет, почему ответ
   ученика неверен и как прийти к уже известному верному ответу.
   Такой подход называется "grounded generation" — генерация с опорой
   на достоверные данные, а не на "чистые знания" модели.

2. В промпт передаётся:
   - текст вопроса
   - вариант, который выбрал ученик (неверный)
   - правильный вариант
   - тема (topic), чтобы объяснение было в контексте школьной программы

3. После разбора отдельных ошибок вызывается ВТОРАЯ функция —
   get_learning_plan(). Она берёт список тем, где были ошибки,
   и просит модель построить короткий пошаговый план: что повторить
   в первую очередь, в каком порядке и какими небольшими шагами.
   Это уже не разбор одного вопроса, а мини-программа обучения —
   ключевая "фишка" для презентации жюри.

4. Промпт жёстко ограничивает тон и длину ответа — ученики 8-12 классов,
   объяснение должно быть коротким, простым и ободряющим, без сложных
   терминов и морализаторства, чтобы не отбить мотивацию учиться.

О КЛЮЧЕ API:
Ключ НИКОГДА не хранится в коде. Он читается из переменной окружения
OPENROUTER_API_KEY — локально она подставляется из файла .env
(через python-dotenv), а на Streamlit Cloud — из Secrets в настройках
приложения. Так ключ не попадает в Git и не утекает на GitHub.
"""

import os
from dotenv import load_dotenv
from openai import OpenAI

load_dotenv()  # подтягивает переменные из локального .env, если он есть


def _get_api_key():
    """
    Достаёт ключ из окружения. Сначала пробуем обычную переменную
    окружения (локальная разработка через .env), потом — Streamlit
    Secrets (там, где приложение задеплоено на Streamlit Cloud).
    """
    key = os.environ.get("OPENROUTER_API_KEY")
    if key:
        return key
    try:
        import streamlit as st
        return st.secrets.get("OPENROUTER_API_KEY")
    except Exception:
        return None


client = OpenAI(
    base_url="https://openrouter.ai/api/v1",
    # Если ключ не найден, подставляем заглушку: клиент создастся без ошибок,
    # а понятная ошибка появится только при реальном вызове API — и тогда
    # сработает fallback в except ниже, а не аварийное завершение всего приложения.
    api_key=_get_api_key() or "missing-api-key",
)

MODEL_NAME = "anthropic/claude-sonnet-5"

# --- НАСТРОЙКИ СКОРОСТИ ---
# max_tokens ограничивает максимальную длину ответа - модель не может
# "растекаться" длиннее, чем реально нужно для короткого объяснения,
# что само по себе немного ускоряет генерацию.
#
# extra_body с reasoning.effort="low" просит модель тратить минимум
# времени на скрытые "размышления" перед ответом (Sonnet 5 поддерживает
# adaptive thinking с разными уровнями effort). Для короткого объяснения
# школьной ошибки глубокие раздумья не нужны - лишняя секунда-две уходит
# на них незаметно для пользователя, но заметно по времени ожидания.
#
# Обёрнуто в try/except: если конкретная модель/провайдер не поддерживает
# этот параметр, код просто повторит запрос без него - fallback безопасен.
FAST_EXTRA_BODY = {"reasoning": {"effort": "low"}}


def _fast_completion(messages: list, max_tokens: int):
    """
    Обёртка над вызовом API с настройками на скорость.
    При первой попытке просим минимальный reasoning effort;
    если провайдер отклонит параметр - тихо повторяем без него.
    """
    try:
        return client.chat.completions.create(
            model=MODEL_NAME,
            messages=messages,
            max_tokens=max_tokens,
            extra_body=FAST_EXTRA_BODY,
        )
    except Exception:
        return client.chat.completions.create(
            model=MODEL_NAME,
            messages=messages,
            max_tokens=max_tokens,
        )


SYSTEM_PROMPT = """Ты — доброжелательный школьный репетитор для учеников 8-12 классов
из сельских школ. Твоя задача — объяснить ученику, почему его ответ на тестовый
вопрос неверен, и показать короткий, понятный путь к правильному ответу.

Правила:
- Пиши простым языком, без сложных терминов без объяснения.
- Не критикуй и не морализируй, объясняй по-доброму, как хороший учитель.
- Объяснение должно быть коротким: 3-5 предложений.
- НЕ здоровайся и не используй вступительные фразы вроде "Привет!", "Здравствуй!",
  "Отличный вопрос!" — сразу переходи к сути объяснения. Ученик получает
  несколько таких объяснений подряд в рамках одного теста, и повторяющиеся
  приветствия выглядят неестественно.
- В конце обязательно укажи, к какой теме школьной программы относится вопрос,
  чтобы ученик знал, что повторить.
- Не придумывай факты, которых нет в предоставленном вопросе."""

PLAN_SYSTEM_PROMPT = """Ты — опытный методист и репетитор для учеников 8-12 классов
из сельских школ, где часто не хватает учителей-предметников. По списку тем,
в которых ученик ошибся, ты составляешь короткую персональную программу
обучения — по шагам, чтобы ученик мог заниматься самостоятельно.

Правила:
- Пиши простым языком, дружелюбно и мотивирующе.
- НЕ здоровайся и не используй вступительные фразы вроде "Привет!" — сразу
  переходи к плану.
- Структурируй план по шагам (3-5 шагов), каждый шаг — 1-2 предложения:
  что именно повторить/сделать, зачем это нужно.
- Расставь темы в порядке от базовых к более сложным, если это уместно.
- Не придумывай учебники или конкретные параграфы, которых нет в задаче.
- В конце добавь короткую фразу поддержки."""


def get_ai_explanation(question_text: str, topic: str,
                        student_answer: str, correct_answer: str) -> str:
    """
    Основная функция ИИ-тьютора для одного вопроса.
    Вызывается ТОЛЬКО для тех вопросов, где ученик ответил неправильно —
    это экономит вызовы API и делает демо быстрее.

    Возвращает текст персонального объяснения.
    """

    user_prompt = f"""
Вопрос теста: {question_text}
Тема: {topic}
Ответ ученика (неверный): {student_answer}
Правильный ответ: {correct_answer}

Объясни ученику, почему его ответ неверен, и как правильно рассуждать,
чтобы прийти к верному ответу.
"""

    try:
        response = _fast_completion(
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=300,
        )
        return response.choices[0].message.content.strip()

    except Exception as e:
        # На хакатоне интернет/API может подвести — обязательно нужен fallback,
        # чтобы демо не сломалось на глазах у жюри.
        return (
            "Не удалось получить объяснение от ИИ-тьютора "
            f"(проверьте API-ключ или соединение). Техническая причина: {e}\n\n"
            f"Правильный ответ: {correct_answer}. Тема для повторения: {topic}."
        )


def get_summary_feedback(subject: str, weak_topics: list) -> str:
    """
    Короткое резюме по итогам теста — одно-два предложения о том,
    над чем стоит поработать в целом. Вызывается один раз в конце теста.
    """
    if not weak_topics:
        return "Отличный результат! Явных пробелов не выявлено."

    topics_str = ", ".join(weak_topics)

    user_prompt = f"""
Ученик проходил тест по предмету "{subject}".
Он допустил ошибки в темах: {topics_str}.

Дай короткую (2-3 предложения) мотивирующую рекомендацию,
что стоит повторить в первую очередь и почему это важно.
"""

    try:
        response = _fast_completion(
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=150,
        )
        return response.choices[0].message.content.strip()
    except Exception:
        return f"Рекомендуем повторить темы: {topics_str}."


def get_learning_plan(subject: str, weak_topics: list) -> str:
    """
    Полноценный персональный план обучения по темам, в которых ученик
    ошибся. В отличие от get_summary_feedback (пара предложений),
    здесь модель строит пошаговую программу — что и в каком порядке
    повторять, с краткими пояснениями по каждому шагу.

    weak_topics — список тем с ошибками (могут повторяться, если
    ошибок в одной теме было несколько — это нормально, модель сама
    сведёт их в план).
    """
    if not weak_topics:
        return "По итогам теста явных пробелов не выявлено — план обучения не требуется."

    # Убираем дубликаты тем, сохраняя порядок появления
    unique_topics = list(dict.fromkeys(weak_topics))
    topics_str = ", ".join(unique_topics)

    user_prompt = f"""
Предмет: {subject}
Темы, в которых ученик допустил ошибки на пробном тесте: {topics_str}

Составь короткую персональную программу обучения (план из 3-5 шагов),
чтобы ученик мог самостоятельно закрыть эти пробелы.
"""

    try:
        response = _fast_completion(
            messages=[
                {"role": "system", "content": PLAN_SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=500,
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        return (
            "Не удалось построить план обучения "
            f"(проверьте API-ключ или соединение). Техническая причина: {e}\n\n"
            f"Темы для самостоятельного повторения: {topics_str}."
        )
