"""
ai_tutor.py
-----------
Это "мозг" продукта — модуль ИИ-тьютора.

ЗАЧЕМ ОН НУЖЕН (для защиты перед жюри):
Обычный тест просто показывает "верно/неверно". Наш продукт решает
реальную проблему сельских школ — нехватку учителей-предметников,
которые могли бы индивидуально разобрать ошибку каждого ученика.
ИИ-тьютор выполняет именно эту функцию: не даёт готовый ответ,
а объясняет ХОД РАССУЖДЕНИЯ, как если бы рядом сидел репетитор.

КАК ЭТО РАБОТАЕТ ТЕХНИЧЕСКИ:
1. Мы НЕ отправляем в LLM вопрос "реши задачу" — мы уже знаем правильный
   ответ (он есть в банке вопросов). Это важно: модель не выдумывает
   правильный ответ заново (риск галлюцинаций), а только объясняет,
   почему ответ ученика неверен и как прийти к уже известному верному ответу.
   Такой подход называется "grounded generation" — генерация с опорой
   на достоверные данные, а не на "чистые знания" модели.

2. В промпт передаётся:
   - текст вопроса
   - вариант, который выбрал ученик (неправильный)
   - правильный вариант
   - тема (topic), чтобы объяснение было в контексте школьной программы

3. Промпт жёстко ограничивает тон и длину ответа — ученики 8-12 классов,
   объяснение должно быть коротким, простым и ободряющим, без сложных
   терминов и морализаторства ("ты должен был..."), чтобы не отбить
   мотивацию учиться.

4. Для MVP используется один вызов API на один неправильный ответ.
   Это не самый дешёвый вариант по токенам, но зато самый простой
   и надёжный для хакатона: каждое объяснение изолировано и его легко
   продемонстрировать вживую на конкретном примере.
"""

