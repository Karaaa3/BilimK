def get_ai_explanation(question_text: str, topic: str, student_answer: str, correct_answer: str) -> str:
    """
    Основная функция ИИ-тьютора.

    Вызывается ТОЛЬКО для тех вопросов, где ученик ответил неправильно —
    это экономит вызовы API и делает демо быстрее (важно на хакатоне,
    где счёт идёт на секунды на сцене).

    Возвращает текст персонального объяснения.
    """

    user_prompt = f"""
Вопрос теста: {question_text}
Тема: {topic}
Ответ ученика (неверный): {student_answer}
Правильный ответ: {correct_answer}

Объясни ученику, почему его ответ неверен, и как правильно рассуждать,
чтобы прийти к верному ответу.
"""

    try:
        # Используем формат OpenRouter/OpenAI
        response = client.chat.completions.create(
            model="anthropic/claude-3.5-sonnet",
            # Модель Claude через OpenRouter
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user}  # Переменная user должна содержать сам промпт
            ]
        )
        # Ответ приходит в другом формате, извлекаем его так
        return response.choices[0].message.content.strip()

    except Exception as e:
        # Обработка ошибок, если API недоступно
        print(f"Ошибка ИИ-тьютора: {e}")
        return (
        "Не удалось получить объяснение от ИИ-тьютора (проверьте API-ключ или соединение).\n"
        f"Техническая причина: {e}\n\n"
        f"Правильный ответ: {correct_answer}. Тема для повторения: {topic}."
    )


def get_summary_feedback(subject: str, weak_topics: list) -> str:
    """
    Дополнительная (опциональная) функция: краткое резюме по итогам теста —
    "над чем стоит поработать в целом". Можно вызывать один раз в конце теста,
    а не на каждый вопрос — хорошая точка роста для презентации ("а ещё у нас
    есть сводная рекомендация").

    weak_topics — список тем, по которым были ошибки.
    """
    if not weak_topics:
        return "Отличный результат! Явных пробелов не выявлено."

    topics_str = ", ".join(weak_topics)

    user_prompt = f"""
Ученик проходил тест по предмету "{subject}".
Он допустил ошибки в темах: {topics_str}.

Дай короткую (2-3 предложения) мотивирующую рекомендацию,
что стоит повторить в первую очередь и почему это важно.
"""

    try:
        response = client.messages.create(
            model="claude-sonnet-4-5",
            max_tokens=200,
            system=SYSTEM_PROMPT,
            messages=[{"role": "user", "content": user_prompt}],
        )
        return response.content[0].text.strip()
    except Exception:
        return f"Рекомендуем повторить темы: {topics_str}."
